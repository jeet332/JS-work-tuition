# 4 types of methods have in bitwise operators.(And &,or |,xor ^,not ~,Lshift,Rshift)
a = 5  # binary: 101
b = 3  # binary: 011

print("AND:", a & b)     # 1
print("OR:", a | b)      # 7
print("XOR:", a ^ b)     # 6
print("NOT a:", ~a)      # -6
print("Left Shift:", a << 1)  # 10
print("Right Shift:", a >> 1) # 2






